class Variables{
    public static void main(String args[]){
        int a = 10;
        int b = 5;
        String name = "Jhansi";
        System.out.println(a);
        System.out.println(b);
        System.out.println(name);
        //swap
        int c = a;
        a = b;
        b = c;
        System.out.println(a);
        System.out.println(b);

    }
}
